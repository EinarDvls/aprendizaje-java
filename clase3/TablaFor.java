public class TablaFor{

    public static void main(String[] args){

        int tabla = 7;//tabla del 7
        
        for(int x = 1; x<=10 ; x++ ){

            int multiplicacion = tabla * x;

            System.out.println(tabla + " x " + x + " = " + multiplicacion);

        }

        System.out.println("Termino");

    }

}