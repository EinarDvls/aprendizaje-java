public class Comparacion{

    public static void main(String[] args) {
        
        if(8 == 9){
            System.out.println("8 es igual a nueve");
        }

        if(9 < 4){
            System.out.println("9 es menor a 4");
        }

        if(0 > -4){
            System.out.println("0 es mayor que -4");
        }

        if(9 <= 20){
            System.out.println("9 es menor o igual a 20");
        }

        if(3 >= 6){
            System.out.println("3 es mayor o igual a 6");
        }

        if(4 != 4){
            System.out.println("4 es diferente de 4");
        }
    }
}